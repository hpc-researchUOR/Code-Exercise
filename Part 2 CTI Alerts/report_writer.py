# Sample selection
# ─────────────────────────────────────────────────────────────────────────────

def pick_best_sample(alerts: list[dict]) -> int:
    """
    Return the index of the highest-confidence non-benign alert.
    Falls back to index 0 if all samples are benign.
    """
    best_idx, best_conf = 0, -1.0
    for i, alert in enumerate(alerts):
        cls  = alert["predicted_threat"]["class_name"]
        conf = alert["predicted_threat"]["confidence"]
        if cls != "Benign" and conf > best_conf:
            best_conf, best_idx = conf, i
    return best_idx


# ─────────────────────────────────────────────────────────────────────────────
# Markdown rendering helpers
# ─────────────────────────────────────────────────────────────────────────────

def _safe(enrichment: dict, key: str, fallback: str = "N/A") -> str:
    val = enrichment.get(key, fallback)
    return str(val).strip() if val else fallback


def _alternatives_table(alternatives: list[dict]) -> str:
    if not alternatives:
        return "_No alternative predictions available._"
    header = "| Class       | Probability |\n|-------------|-------------|"
    rows   = "\n".join(
        f"| {a['class_name']:11s} | {a['probability']*100:.2f}%       |"
        for a in alternatives
    )
    return f"{header}\n{rows}"


def _observations_table(obs: dict) -> str:
    if not obs:
        return "_No network observations available._"
    header = "| Feature | Value |\n|---------|-------|"
    rows   = "\n".join(f"| `{k}` | `{v}` |" for k, v in obs.items())
    return f"{header}\n{rows}"


def _severity_icon(reasonable: bool) -> str:
    return "✅ Reasonable" if reasonable else "⚠️ Questionable"


def _match_icon(match: bool) -> str:
    return "✅ Matched" if match else "⚠️ Not clearly matched"


def _hallucination_icon(count: int, detail: str) -> str:
    if count == 0:
        return "✅ None detected"
    return f"⚠️ {count} detected: {detail}"


